export default class calculator {
    static Sum(a: number, b: number): number {
      let c = a + b;
      return c;
    }
  
    static Difference(a: number, b: number): number {
      let c = a - b;
      return c;
    }

    static Multiply(a: number, b: number): number {
      let c = a * b;
      return c;
    }

    static Divide(a:number, b:number): number {
      let c = a / b;
      return c;
    }
  }